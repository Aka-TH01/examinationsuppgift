<h1 style="text-align:center"> Vad är Kyokushin </h1>




<p style="text-align:center">Kyoku betyder ytterst, shin betyder sanning eller verklighet och kai sammanslutning eller att ansluta sig till. Översatt blir Kyokushinkai Den yttersta sanningens sammanslutning. Tillägget Kan betyder hus eller skola. Kyokushinkai är en modern karatestil där grundtekniker och former hämtats från traditionell karate, medan fightingen utvecklats med inslag av kinesiska kamparter. Kampen har alltid prioriterats i kyokushin karate där teori och taktik bygger på principen om cirkeln och punkten. Cirkulära rörelser framför linjära och kontinuitet i kampen. I kyokushin har alltid vilja och kraft betonats.</p>




<p style="text-align:center"> Träningen hjälper dig att utvecklas både mentalt och fysiskt. Ett led i utvecklingen kan vara att tävla för att mäta sig med andra inom samma karatestil. Inom kyokushin kan du tävla i både kumite – dvs. kamp och inom kata – form. Inom kumite finns det olika tävlingar. Mest känt är kyokushin för sina hårda knock-down tävlingar internationellt. Andra tävlingar i Sverige är dock oftast i form av semikontakt för alla åldersklasser. Ett annat naturligt inslag inom kyokushin är tamashiwari, dvs krossteknik. Tamashiwari tränas och tillämpas även inom tävlingsverksamhet. Då gäller det att ett minimum av tre plankor måste brytas med vardera seiken (knytnäve), shuto (handkant), hiji (armbåge) och kakato (hål). Det förekommer även att man tillämpar krossteknik på betong, is, flaskor och andra träföremål. </p>


